let myArray = [1, 2, 3];

console.log(myArray.toString()); // "1, 2, 3"
console.log(myArray); // [1, 2, 3]

let fun = (function (x) { f(x); });
console.log(fun.toString()); // function (x) { f(x); }

let date = new Date(2023, 5, 3);
console.log(date.toString()); // Sat Jun 03 2023 00:00:00 GMT+0200 (South Africa Standard Time)